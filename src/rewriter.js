/** Переписывает заголовок и текст статьи через OpenAI, чтобы получить оригинальный контент для Дзена. */
async function rewriteArticle(article) {
  if (!config.openai.apiKey) {
    throw new Error('OPENAI_API_KEY не задан');
  }

  const userPrompt = `Заголовок: ${article.title}\n\nТекст: ${article.text || ''}`;

  const MAX_ATTEMPTS = 3;
  for (let attempt = 1; attempt <= MAX_ATTEMPTS; attempt++) {
    try {
      const res = await axios.post(
        `${config.openai.baseURL}/chat/completions`,
        {
          model: config.openai.model,
          messages: [
            { role: 'system', content: SYSTEM_PROMPT },
            { role: 'user',   content: userPrompt },
          ],
          response_format: { type: 'json_object' },
          temperature: 0.7,
        },
        {
          headers: {
            Authorization:  `Bearer ${config.openai.apiKey}`,
            'Content-Type': 'application/json',
          },
          timeout: 60000,
        },
      );

      const raw = res.data?.choices?.[0]?.message?.content;
      if (!raw) throw new Error('OpenAI: пустой ответ');

      const parsed = JSON.parse(raw);
      if (!parsed.title || !parsed.text) throw new Error('OpenAI: ответ без title/text');

      return { title: parsed.title, text: parsed.text };
    } catch (e) {
      const detail = e.response?.data?.error?.message || e.message || String(e);
      if (attempt < MAX_ATTEMPTS) {
        logger.warn(`OpenAI: попытка ${attempt}/${MAX_ATTEMPTS} не удалась (${detail}). Повтор через 5с...`);
        await new Promise((r) => setTimeout(r, 5000));
        continue;
      }
      throw new Error(`OpenAI: ${detail}`);
    }
  }
}

module.exports = { rewriteArticle };
